        dsc.setDriverName("oracle.jdbc.driver.OracleDriver");
        dsc.setUsername("kingdee");
        dsc.setPassword("kingdee");
        dsc.setDbType(DbType.ORACLE);
        mpg.setDataSource(dsc);

